-- Implementation of tables specified in database design 

-- Create the db
DROP DATABASE IF EXISTS Group3DB;
CREATE DATABASE IF NOT EXISTS Group3DB;
USE Group3DB;

-- Create CUSTOMER table
CREATE TABLE IF NOT EXISTS CUSTOMER (
    Customer_ID VARCHAR(255) PRIMARY KEY,
    First_Name VARCHAR(255) NOT NULL,
    Last_Name VARCHAR(255) NOT NULL,
    Email VARCHAR(255) UNIQUE,
    Phone_No VARCHAR(10) UNIQUE
);

-- Create VEHICLE table
CREATE TABLE IF NOT EXISTS VEHICLE (
    License_Plate VARCHAR(7) PRIMARY KEY,
    Make VARCHAR(255),
    Model VARCHAR(255),
    Year CHAR(4),
    Color VARCHAR(255),
    Customer_ID VARCHAR(255),
    FOREIGN KEY (Customer_ID) REFERENCES CUSTOMER(Customer_ID)
);

-- Create EMPLOYEE table
CREATE TABLE IF NOT EXISTS EMPLOYEE (
    Emp_ID VARCHAR(255) PRIMARY KEY,
    Name VARCHAR(255) NOT NULL,
    Address VARCHAR(255),
    Phone_No CHAR(10),
    Salary DECIMAL(10,2)
);

-- Create LOCATION table
CREATE TABLE IF NOT EXISTS LOCATION (
    Store_No VARCHAR(5) PRIMARY KEY,
    Address VARCHAR(255),
    Phone_No CHAR(10) UNIQUE
);

-- Create SERVICE table
CREATE TABLE IF NOT EXISTS SERVICE (
    Service_ID VARCHAR(255) PRIMARY KEY,
    Wash_Type VARCHAR(255),
    Price DECIMAL(10,2)
);

-- Create SUPPLIES table
CREATE TABLE IF NOT EXISTS SUPPLIES (
    Item_No CHAR(4) PRIMARY KEY,
    Item_Name VARCHAR(255),
    Quantity INT
);

-- Create TRANSACTION table
CREATE TABLE IF NOT EXISTS TRANSACTION (
    Trans_ID VARCHAR(255) PRIMARY KEY,
    Date DATE NOT NULL,
    Total_Cost DECIMAL(10,2),
    Emp_ID VARCHAR(255),
    Store_No VARCHAR(5),
    License_Plate VARCHAR(7),
    FOREIGN KEY (Emp_ID) REFERENCES EMPLOYEE(Emp_ID),
    FOREIGN KEY (Store_No) REFERENCES LOCATION(Store_No)
);

-- Create TRANSACTION_SERVICE table
CREATE TABLE IF NOT EXISTS TRANSACTION_SERVICE (
    Trans_ID VARCHAR(255),
    Service_ID VARCHAR(255),
    PRIMARY KEY (Trans_ID, Service_ID),
    FOREIGN KEY (Trans_ID) REFERENCES TRANSACTION(Trans_ID),
    FOREIGN KEY (Service_ID) REFERENCES SERVICE(Service_ID)
);

-- Create SERVICE_SUPPLIES Table
CREATE TABLE IF NOT EXISTS SERVICE_SUPPLIES (
    Service_ID VARCHAR(255),
    Item_No CHAR(4),
    PRIMARY KEY (Service_ID, Item_No),
    FOREIGN KEY (Service_ID) REFERENCES SERVICE(Service_ID),
    FOREIGN KEY (Item_No) REFERENCES SUPPLIES(Item_No)
);

CREATE TABLE IF NOT EXISTS RECEIVES_SERVICE (
    Service_ID VARCHAR(255),
    License_Plate VARCHAR(7),
    PRIMARY KEY (Service_ID, License_Plate),
    FOREIGN KEY (Service_ID) REFERENCES SERVICE(Service_ID),
    FOREIGN KEY (License_Plate) REFERENCES VEHICLE(License_Plate)
);

